package Java_Tutorial;

public class Lesson8_JavaStrings {

    public static void main(String []args) {
        /*
            __ Java Strings __
            Strings are used for storing text.

            A String variable contains a collection of characters surrounded by double quotes. Example:

            ```
            String myName = "Carlos";
            String myLastName = "Souza";
            System.out.println(myName + " " + myLastName);
            ```

            A String in Java is actually an object, which contain methods that can perform certain operations
            on strings. For example, the length of a string can be found with the length() method:

            ```
            String word = "alphabetic";
            System.out.println("word length: " + word.length());
            ```

            There are many string methods available, for example toUpperCase() and toLowerCase():

            ```
            String language = "English";
            System.out.println(language.toUpperCase());
            System.out.println(language.toLowerCase());
            ```

            The indexOf() method returns the index (the position) of the first occurrence of a
            specified text in a string (including whitespace):

            ```
            String address = "rue de Bergerac, Terrebonne J6Y 1E7";
            System.out.println(address.indexOf("Bergerac"));   // Output: 7
            ```

            Six other escape sequences are valid in Java:

                Code	Result
                \n	    New Line
                \r	    Carriage Return
                \t	    Tab
                \b	    Backspace
                \f	    Form Feed

         */

    }
}
