package Java_Tutorial;

public class Lesson4_JavaVariables {

    public static void main(String []args) {

        /*
            # Java Variables:

            Variables are containers for storing data values.

            In Java, there are different types of variables, for example:

                1- String - stores text, such as "Hello". String values are surrounded by double quotes
                2- int - stores integers (whole numbers), without decimals, such as 123 or -123
                3- float - stores floating point numbers, with decimals, such as 19.99 or -19.99
                4- char - stores single characters, such as 'a' or 'B'. Char values are surrounded by single quotes
                5- boolean - stores values with two states: true or false

                NOTE: Just like we do in the C language, we first declare the variable's type,
                name the variable, and then insert its contents. Example:

                ```
                int num = 10;
                String job = "Developer";
                boolean result = false;
                ```

                NOTE: if you assign a new value to an existing variable,
                it will overwrite the previous value. Example:

                ```
                int value = 20;
                value = 10;
                System.out.println(value);   // Output: 10
                ```

                NOTE: The 'final' global word makes your variable immutable (its value cannot be
                changed during code execution). Example:

                ```
                final float number = 9.99f;   // the variable 'number' will be '9.99' always!
                System.out.println(number)
                ```

                ** Declare Many Variables **

                To declare more than one variable of the same type, use a comma-separated list. Example:

                ```
                int x = 5, y = 10, z = 2;
                System.out.println(x * y * z);   // Output: 100
                ```
         */

    }
}
